const rawEntries = rawVocabulary as RawVocabularyWord[];
const configuredWordCount = chapterDefinitions.reduce((sum, chapter) => sum + chapter.wordCount, 0);

if (configuredWordCount !== rawEntries.length) {
  throw new Error(`Chapter mapping word count mismatch: expected ${rawEntries.length}, got ${configuredWordCount}.`);
}

export const vocabularyDatabase: VocabularyWord[] = [];

let rawIndex = 0;

for (const definition of chapterDefinitions) {
  for (let count = 0; count < definition.wordCount; count += 1) {
    const entry = rawEntries[rawIndex];

    vocabularyDatabase.push({
      id: `chapter-${String(definition.chapter).padStart(2, '0')}-word-${String(entry.no).padStart(4, '0')}`,
      chapter: definition.chapter,
      word: entry.word.trim(),
      phonetic: entry.uk_pronunciation || entry.us_pronunciation || '',
      audio: '',
      part_of_speech: entry.pos.trim(),
      meaning: entry.explanation.trim() || entry.definition_raw.trim(),
      example: '',
      word_family: supplementalWordFamilies[entry.word.trim().toLowerCase()] ?? [],
      collocations: [],
      memory: [...initialMemoryBoxes] as MemoryBoxes,
      spelling: { ...initialSpellingStats },
      memoryMarks: [...initialMemoryMarks],
      memoryHistory: [],
    });

    rawIndex += 1;
  }
}

const wordsByChapterMap = new Map<number, VocabularyWord[]>();

for (const word of vocabularyDatabase) {
  const chapterWords = wordsByChapterMap.get(word.chapter);
  if (chapterWords) {
    chapterWords.push(word);
  } else {
    wordsByChapterMap.set(word.chapter, [word]);
  }
}

export const vocabularyChapterSummaries: VocabularyChapterSummary[] = chapterDefinitions.map((definition) => {
  const words = wordsByChapterMap.get(definition.chapter) ?? [];
  const learnedWords = words.filter((word) => word.memory.some(Boolean)).length;

  return {
    chapter: definition.chapter,
    chapterLabel: String(definition.chapter).padStart(2, '0'),
    topic: definition.topic,
    learnedWords,
    totalWords: words.length,
    progress: words.length === 0 ? 0 : Math.round((learnedWords / words.length) * 100),
  };
});

export const vocabularyStats = {
  totalWords: vocabularyDatabase.length,
  totalChapters: vocabularyChapterSummaries.length,
  todayLearned: 0,
  todayReviewed: 0,
};

export function getWordsByChapter(chapter: number) {
  return wordsByChapterMap.get(chapter) ?? [];
}
